-- Oracle 19c schema objects for encrypted model-monitoring telemetry and metrics.
-- Run as the monitoring schema owner. Oracle TDE must be enabled by the DBA for
-- the tablespace and backups in addition to application-level payload encryption.

CREATE TABLE ML_PREDICTION_TELEMETRY (
    TELEMETRY_ID       VARCHAR2(36) PRIMARY KEY,
    MODEL_ID           VARCHAR2(50) NOT NULL,
    ARTIFACT_SHA256    VARCHAR2(64) NOT NULL,
    RECORDED_AT        TIMESTAMP WITH TIME ZONE NOT NULL,
    EXPIRES_AT         TIMESTAMP WITH TIME ZONE NOT NULL,
    KEY_ID             VARCHAR2(16) NOT NULL,
    ENCRYPTED_PAYLOAD  BLOB NOT NULL,
    PAYLOAD_SHA256     VARCHAR2(64) NOT NULL,
    CREATED_AT         TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT CK_ML_TELEM_EXPIRY CHECK (EXPIRES_AT > RECORDED_AT)
);

CREATE INDEX IX_ML_TELEM_MODEL_TIME
    ON ML_PREDICTION_TELEMETRY (MODEL_ID, ARTIFACT_SHA256, RECORDED_AT);
CREATE INDEX IX_ML_TELEM_EXPIRY ON ML_PREDICTION_TELEMETRY (EXPIRES_AT);

CREATE TABLE ML_MONITOR_BASELINE (
    BASELINE_ID         VARCHAR2(36) PRIMARY KEY,
    MODEL_ID            VARCHAR2(50) NOT NULL,
    ARTIFACT_SHA256     VARCHAR2(64) NOT NULL,
    BASELINE_JSON       CLOB NOT NULL,
    IS_ACTIVE           CHAR(1) DEFAULT 'Y' NOT NULL,
    CREATED_AT          TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT CK_ML_BASELINE_ACTIVE CHECK (IS_ACTIVE IN ('Y', 'N')),
    CONSTRAINT CK_ML_BASELINE_JSON CHECK (BASELINE_JSON IS JSON)
);

CREATE UNIQUE INDEX UX_ML_BASELINE_ACTIVE
    ON ML_MONITOR_BASELINE (CASE WHEN IS_ACTIVE = 'Y' THEN MODEL_ID END);

CREATE TABLE ML_DRIFT_REPORT (
    REPORT_ID          VARCHAR2(36) PRIMARY KEY,
    MODEL_ID           VARCHAR2(50) NOT NULL,
    ARTIFACT_SHA256    VARCHAR2(64) NOT NULL,
    WINDOW_START       TIMESTAMP WITH TIME ZONE NOT NULL,
    WINDOW_END         TIMESTAMP WITH TIME ZONE NOT NULL,
    RECORD_COUNT       NUMBER(12) NOT NULL,
    OVERALL_STATUS     VARCHAR2(20) NOT NULL,
    REPORT_JSON        CLOB NOT NULL,
    CREATED_AT         TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT CK_ML_DRIFT_WINDOW CHECK (WINDOW_END > WINDOW_START),
    CONSTRAINT CK_ML_DRIFT_JSON CHECK (REPORT_JSON IS JSON)
);

CREATE INDEX IX_ML_DRIFT_MODEL_TIME
    ON ML_DRIFT_REPORT (MODEL_ID, ARTIFACT_SHA256, WINDOW_END);

CREATE OR REPLACE PROCEDURE PURGE_EXPIRED_ML_TELEMETRY
AUTHID DEFINER
AS
BEGIN
    DELETE FROM ML_PREDICTION_TELEMETRY WHERE EXPIRES_AT <= SYSTIMESTAMP;
    COMMIT;
END;
/

BEGIN
    DBMS_SCHEDULER.CREATE_JOB(
        JOB_NAME        => 'JOB_PURGE_EXPIRED_ML_TELEM',
        JOB_TYPE        => 'STORED_PROCEDURE',
        JOB_ACTION      => 'PURGE_EXPIRED_ML_TELEMETRY',
        START_DATE      => SYSTIMESTAMP,
        REPEAT_INTERVAL => 'FREQ=DAILY;BYHOUR=2;BYMINUTE=0;BYSECOND=0',
        ENABLED         => TRUE,
        COMMENTS        => 'Deletes encrypted prediction telemetry after approved retention expiry'
    );
END;
/
